def almost_equal_tolerance(a: float, b: float, tolerance: float = 1e-9) -> bool:
    """
    Checks if two numbers are approximately equal within a given tolerance.

    Args:
        a (float): First number.
        b (float): Second number.
        tolerance (float, optional): The allowable difference. Default is 1e-9.

    Returns:
        bool: True if numbers are close enough, False otherwise.
    """
    return abs(a - b) <= tolerance

